/**
 * _strcmp - function compares two strings
 *
 * @s1: string one
 * @s2: string two
 *
 * Return: result
 */

int _strcmp(char *s1, char *s2)
{
	int i = 0, j = 0, cmp = s1[i] - s2[j];

	if (s1[i] < s2[j])
	{
		return (cmp);
	}

	else if (s1[i] > s2[j])
	{
		return (cmp);
	}
	else
	{
		return (cmp);
	}

}
